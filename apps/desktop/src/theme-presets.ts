export function applyThemePresetToRoot(root: HTMLElement, presetId: ThemePresetId, resolvedTheme: ResolvedTheme): void {
  for (const tokenName of themeTokenNames) {
    root.style.removeProperty(tokenName);
  }

  const preset = themePresets.find((candidate) => candidate.id === presetId);
  if (!preset || preset.id === "default") {
    root.dataset.themePreset = "default";
    return;
  }

  root.dataset.themePreset = preset.id;
  const tokens = preset.variants[resolvedTheme].tokens;
  for (const [tokenName, value] of Object.entries(tokens)) {
    root.style.setProperty(tokenName, value);
  }
}

function preset(
  id: ThemePresetId,
  name: string,
  description: string,
  swatches: readonly string[],
  light: ThemeTokenMap,
  dark: ThemeTokenMap,
): ThemePreset {
  return {
    id,
    name,
    description,
    swatches,
    variants: {
      light: { label: `${name} light`, tokens: light },
      dark: { label: `${name} dark`, tokens: dark },
    },
  };
}

function withOverrides(base: ThemeTokenMap, overrides: ThemeTokenMap): ThemeTokenMap {
  return {
    ...base,
    ...overrides,
  };
}
